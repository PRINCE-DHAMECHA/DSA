//~ Topological Sort
//* Linear ordering of vertices such that for every directed edge uv, vertex u comes before v in ordering.
//* Only possible for DAG
//* If cycle then there's non meaning of order as it can be repeat

//~ Algorithms:-
//* Kahn's Algorithm
//* Modified DFS

//~ Applications:-
//* Job Scheduling by OS
//* Maven Dependency Resolution
//* Order of load tables with foreign keys in DB

//~ Time:- O(V+E)